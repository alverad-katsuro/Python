def main():
	print ("Digite 3 números a seguir para saber se são Crescentes ou Decrescente")
	x = int(input("Digite o 1º número\n"))
	y = int(input("Digite o 2º número\n"))
	z = int(input("Digite o 3º número\n"))
	if x < y < z:
	 print ("Crescente")
	else:
	 print ("Não esta em ordem crescente")
main()